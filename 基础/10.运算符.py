#!/usr/bin/python3

# ============== 1. 算术运算符 ==============
print("===== 1. 算术运算符 =====")
a = 10
b = 3
print(f"a = {a}, b = {b}")
print("加法 +:", a + b)
print("减法 -:", a - b)
print("乘法 *:", a * b)
print("除法 /:", a / b)
# 整除
print("整除 //:", a // b)
# 取余
print("取余 %:", a % b)
# 幂运算
print("幂运算 **:", a ** b)

# ============== 2. 赋值运算符 ==============
print("\n===== 2. 赋值运算符 =====")
x = 20
# 加法赋值
x += 5
print("x +=5 →", x)
# 减法赋值
x -= 3
print("x -=3 →", x)
# 乘法赋值
x *= 2
print("x *=2 →", x)
# 除法赋值
x /= 2
print("x /=2 →", x)
# 取余赋值
x %= 3
print("x %=3 →", x)
# 幂赋值
x **= 2
print("x **=2 →", x)
# 整除赋值
x //= 2
print("x //=2 →", x)

# ============== 3. 比较运算符 ==============
print("\n===== 3. 比较运算符 =====")
m = 15
n = 10
print("等于 ==:", m == n)
print("不等于 !=:", m != n)
print("大于 >:", m > n)
print("小于 <:", m < n)
print("大于等于 >=:", m >= n)
print("小于等于 <=:", m <= n)

# ============== 4. 逻辑运算符 ==============
print("\n===== 4. 逻辑运算符 =====")
p = True
q = False
# 与：都为真才为真
print("and:", p and q)
# 或：一个为真即为真
print("or:", p or q)
# 非：取反
print("not:", not p)

# ============== 5. 成员运算符 ==============
print("\n===== 5. 成员运算符 =====")
lst = [1, 2, 3, 4]
s = "python"
# 判断是否在序列中
print("3 in lst:", 3 in lst)
print("5 in lst:", 5 in lst)
# 判断不在序列中
print("a not in s:", "a" not in s)

# ============== 6. 身份运算符 ==============
print("\n===== 6. 身份运算符 =====")
# is 判断内存地址是否相同，==判断值是否相同
num1 = [1, 2, 3]
num2 = [1, 2, 3]
num3 = num1
print("num1 == num2:", num1 == num2)
print("num1 is num2:", num1 is num2)
print("num1 is num3:", num1 is num3)
print("num1 is not num2:", num1 is not num2)

# ============== 7. 位运算符（针对二进制） ==============
print("\n===== 7. 位运算符 =====")
x = 60  # 二进制 0011 1100
y = 13  # 二进制 0000 1101
print("按位与 &:", x & y)
print("按位或 |:", x | y)
print("按位异或 ^:", x ^ y)
print("按位取反 ~:", ~x)
print("左移 <<:", x << 2)
print("右移 >>:", x >> 2)

# ============== 8. 运算符优先级 ==============
print("\n===== 8. 运算符优先级测试 =====")
# 优先级：幂运算 > 乘除取余 > 加减 > 比较 > 逻辑
res = 10 + 5 * 2 ** 2
print("10 + 5 * 2 ** 2 =", res)
# 括号改变优先级
res2 = (10 + 5) * 2 ** 2
print("(10 + 5) * 2 ** 2 =", res2)