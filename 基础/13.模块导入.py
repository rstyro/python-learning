#!/usr/bin/python3
# Python 模块导入 最全用法（新手必看）

# ======================
# 1. 导入整个模块
# ======================
import math

print("===== 1. import 模块名 =====")
print("圆周率 π =", math.pi)
print("3 的平方 =", math.pow(3, 2))

# ======================
# 2. 导入模块并起别名（简化写法）
# ======================
import random as r

print("\n===== 2. import 模块 as 别名 =====")
print("0~1随机数 =", r.random())

# ======================
# 3. 从模块中导入指定功能
# ======================
from math import sqrt, pi

print("\n===== 3. from 模块 import 功能 =====")
print("π =", pi)
print("16 的平方根 =", sqrt(16))

# ======================
# 4. 从模块导入所有内容（不推荐，但要认识）
# ======================
from math import *

print("\n===== 4. from 模块 import * =====")
print("自然常数 e =", e)

# ======================
# 5. 导入自己写的模块（演示用法）
# ======================
print("\n===== 5. 导入自定义模块 =====")

# 假如你有一个文件叫 mymodule.py
# 内容如下：
# def say_hi():
#     print("你好，我是自定义模块！")

# 导入写法：
# import mymodule
# mymodule.say_hi()

# ======================
# 6. 查看模块里有什么
# ======================
print("\n===== 6. 查看模块内容 =====")
print("math 模块里的东西：", dir(math))
