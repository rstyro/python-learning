import pandas as pd

data = {
    "姓名": ["张三", "李四", "王五", "赵六", "钱七"],
    "成绩": [90, 85, 92, 78, 100],
    "城市": ["北京", "上海", "北京", "深圳", "上海"]
}
df = pd.DataFrame(data)

# 取单列
print("===== 成绩列 =====")
print(df["成绩"])

# 取多列
print("\n===== 姓名+成绩 =====")
print(df[["姓名", "成绩"]])

# 条件筛选
print("\n===== 成绩大于90 =====")
print(df[df["成绩"] > 90])

# 多条件筛选
print("\n===== 北京且成绩≥90 =====")
print(df[(df["城市"] == "北京") & (df["成绩"] >= 90)])